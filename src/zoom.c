#include "lib.h"

void	ft_zoom_in(int x, int y, t_all *a)
{
	a->zoom *= 1.1;
	a->xx += x - (WIDTH / 2);
	a->yy += y - (HEIGHT / 2);
}

void	ft_zoom_out(int x, int y, t_all *a)
{
	a->zoom /= 1.1;
	a->xx += x - (WIDTH / 2);
	a->yy += y - (HEIGHT / 2);
}
